package Com.demo.conditionalstatement;
import java.util.*;

public class Even {

	public static void main(String[] args) {
		Scanner sc=new Scanner(System.in);
		int rem,num;
		System.out.println("Enter the value");
		num=sc.nextInt();
		rem=num%2;
		if (rem==0) {
			System.out.println("even");
		}
		else {
			System.out.println("odd");
		}
				
				
				
		
		

	}

}
