package Com.demo.patterns;

public class assign2_A {
	public static void pattern() {
		int i, j;
		for (i = 1; i <= 5; i++) {
			for (j = 1; j <= 2; j++) {
				System.out.print(1);
				System.out.print(0);

			}
			System.out.println();
		}
	}

	public static void main(String[] args) {
		pattern();

	}

}
